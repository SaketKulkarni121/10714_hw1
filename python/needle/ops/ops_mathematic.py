class BroadcastTo(TensorOp):
    def __init__(self, shape):
        self.shape = shape

    def compute(self, a):
        ### BEGIN YOUR SOLUTION
        return array_api.broadcast_to(a, self.shape)
        ### END YOUR SOLUTION

    def gradient(self, out_grad, node):
        ### BEGIN YOUR SOLUTION
        a = node.inputs[0]
        orig_shape = a.shape
        
        axes_to_sum = []
        for i in range(len(out_grad.shape)):
            if i >= len(orig_shape) or orig_shape[i] != out_grad.shape[i]:
                axes_to_sum.append(i)
        
        if axes_to_sum:
            grad = out_grad.sum(axes=tuple(axes_to_sum))
        else:
            grad = out_grad
        
        return grad.reshape(orig_shape)
        ### END YOUR SOLUTION


def broadcast_to(a, shape):
    return BroadcastTo(shape)(a)


class Summation(TensorOp):
    def __init__(self, axes: Optional[tuple] = None):
        self.axes = axes

    def compute(self, a):
        ### BEGIN YOUR SOLUTION
        return a.sum(self.axes)
        ### END YOUR SOLUTION

    def gradient(self, out_grad, node):
        ### BEGIN YOUR SOLUTION
        a = node.inputs[0]
        orig_shape = a.shape
        
        if self.axes is None:
            return out_grad.broadcast_to(orig_shape)
        
        new_shape = list(orig_shape)
        for axis in self.axes:
            new_shape[axis] = 1
        
        grad = out_grad.reshape(tuple(new_shape))
        
        return grad.broadcast_to(orig_shape)
        ### END YOUR SOLUTION


def summation(a, axes=None):
    return Summation(axes)(a)


class MatMul(TensorOp):
    def compute(self, a, b):
        ### BEGIN YOUR SOLUTION
        return a @ b
        ### END YOUR SOLUTION

    def gradient(self, out_grad, node):
        ### BEGIN YOUR SOLUTION
        a, b = node.inputs[0], node.inputs[1]
        
        d_a = out_grad @ b.transpose()
        d_b = a.transpose() @ out_grad
        
        if len(a.shape) < len(out_grad.shape):
            extra_dims = len(out_grad.shape) - len(a.shape)
            for _ in range(extra_dims):
                d_a = d_a.sum(axes=(0,))
        
        if len(b.shape) < len(out_grad.shape):
            extra_dims = len(out_grad.shape) - len(b.shape)
            for _ in range(extra_dims):
                d_b = d_b.sum(axes=(0,))
        
        return d_a, d_b
        ### END YOUR SOLUTION


def matmul(a, b):
    return MatMul()(a, b)


